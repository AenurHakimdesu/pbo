package pertemuan1;

public class Latih2a {

	public static void main(String[] args) {
		
		double diameter = 10;
		double jarijari = diameter/2;
		double phi = 3.14;
		double keliling = 2 * phi * jarijari;
		
		System.out.println("Menghitung keliling Lingkaran");
		System.out.println("Diketahui sebuah lingkaran memiliki: ");
		System.out.println("Diameter: "+diameter);
		System.out.println("Jari-Jari: "+jarijari);
		System.out.println("Phi: "+phi);
		System.out.println("Jadi keliling lingkaran tersebut adalah= "+keliling);

	}

}
